# ── rgba хелпер для CSS ───────────────────────────────────────────────────────

def _r(hex_color: str, alpha: float) -> str:
    r, g, b = hex_to_rgb(hex_color)
    return f"rgba({r},{g},{b},{alpha:.2f})"


