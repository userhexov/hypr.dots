# ── Reload SwayNC ─────────────────────────────────────────────────────────────

def _reload() -> None:
    from utils import run
    run("swaync-client --reload-config")


# ── Публичный API ─────────────────────────────────────────────────────────────

def generate_style(colors: dict) -> None:
    """
    Генерирует style.css для SwayNC и перезагружает его.
    Пропускает если SwayNC не установлен.
    """
    if not Tools.available("swaync"):
        warn("SwayNC не найден — style.css пропущен")
        return

    SWAYNC_DIR.mkdir(parents=True, exist_ok=True)
    STYLE_FILE.write_text(_build_css(colors))
    ok(f"SwayNC style.css → {STYLE_FILE}")

    _reload()
